package com.spring.ex.scrap.domain;

public class ScrapVO {
	private int scrap_id;
	private String scrap_userId;
	private int scrap_portfolio_id;

	public int getScrap_id() {
		return scrap_id;
	}

	public void setScrap_id(int scrap_id) {
		this.scrap_id = scrap_id;
	}

	public String getScrap_userId() {
		return scrap_userId;
	}

	public void setScrap_userId(String scrap_userId) {
		this.scrap_userId = scrap_userId;
	}

	public int getScrap_portfolio_id() {
		return scrap_portfolio_id;
	}

	public void setScrap_portfolio_id(int scrap_portfolio_id) {
		this.scrap_portfolio_id = scrap_portfolio_id;
	}

}
